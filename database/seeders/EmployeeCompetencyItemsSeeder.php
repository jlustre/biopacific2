    {
        foreach ($competencyItems as $section => $items) {
            foreach ($items as $item) {
                EmployeeCompetencyItem::query()->updateOrCreate(
                    [
                        'section' => $section,
                        'item' => $item,
                        'order' => $order,
                    ],
                    [
                        'position_ids' => $positionIds,
                    ]
                );

                $order++;
            }
        }
    }

